export function chatSystemBlocks(characterName, persona, conditions, mode, scenario, memory) {
  const roleplayPreamble = `你正在運行一份由「女媧蒸餾管線」產出的人物檔案。完全依照檔案中的「角色扮演規則」與「表達DNA」，以「${characterName}」的身份與使用者對話。

防漂移鐵則：
- 長對話中不得逐漸退回通用AI助手的語氣，每次回應前先對照表達DNA
- 遇到檔案未覆蓋的問題，依「核心心智模型」推斷立場，並以此人的方式表達不確定（參照誠實邊界）
- 不編造此人沒說過的話當作引語
- 使用者說「退出角色」時才恢復正常模式`;

  const blocks = [
    { type: 'text', text: roleplayPreamble },
    {
      type: 'text',
      text: `以下是人物檔案：\n\n${persona}`,
      cache_control: { type: 'ephemeral' },
    },
  ];

  // 跨對話記憶:此人物在過往對話中累積記得、關於使用者與彼此關係的事(不進快取前綴,因會變動)
  if (memory && memory.trim()) {
    blocks.push({
      type: 'text',
      text: `【你記得的事(跨對話累積記憶)】
以下是你(${characterName})在過往對話中記得、關於使用者與你們關係的事。自然地把它當成「你本來就記得」,不要每次都重述或說「根據我的記憶」。若記憶與當下對話衝突,以當下為準。

${memory.trim()}`,
    });
  }

  const cond = conditionsBlock(conditions);
  if (cond) blocks.push({ type: 'text', text: cond });

  if (mode === 'training') {
    blocks.push({ type: 'text', text: trainingProtocol(characterName, getScenario(scenario)) });
  } else {
    const protocol = MODE_PROTOCOLS[mode];
    if (protocol) blocks.push({ type: 'text', text: protocol(characterName) });
  }
  return blocks;
}

